    private void eraseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eraseActionPerformed
        
        String before = "";
        String after = "";
        int posicionCursor= 0;
        
        posicionCursor = textIn.getCaretPosition();
        if (textIn.getCaretPosition()>0){
            before = textIn.getText().substring(0,textIn.getCaretPosition()-1);
            after = textIn.getText().substring(textIn.getCaretPosition(),textIn.getText().length());
            
            textIn.setText(""+before+after);
            textIn.setCaretPosition(posicionCursor-=1); // esto es para colocar el cursos despues de lo ultimo que escribi.
  
        }
                
        textIn.requestFocus();
         
        
        
    }//GEN-LAST:event_eraseActionPerformed

