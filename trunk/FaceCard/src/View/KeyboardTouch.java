    /*
     * Lo que hace este metodo es recibir un parametro para el cual 
     * el escibira en el jTextField con el fin de poder escribir. 
     */
    public void write(String letter){
        String letterIn="";
        letterIn = letter;
        String before = "";
        String after = "";
        int posicionCursor= -1;
        
        posicionCursor = textIn.getCaretPosition();
        before = textIn.getText().substring(0,textIn.getCaretPosition());
        if (textIn.getText().length()>0) // Para que no me de problemas
            after = textIn.getText().substring(textIn.getCaretPosition(),textIn.getText().length()-1);
        
                
        if (mayus)
            letterIn = letterIn.toUpperCase();
         
        textIn.setText(""+before+letterIn+after+" ");
        
        
        
        textIn.setCaretPosition(posicionCursor+=1); // esto es para colocar el cursos despues de lo ultimo que escribi.
        
        textIn.requestFocus();
         
    }
    
    // Lo siguiente es un hilo, el cual solo se encarga 
    // de ver si alguna tecla es presionada por mas de dos segundos de ser asi muestra 
    // opciones de mas que puede dar a cada tecla. 
    public class PresionBoton extends Thread{

        private int quartSecond =0 ; // en 0 empieza la cuenta
        public void run (){
           while (isPressed) {  // Quiere decir que mientras este presionado, voy a estar contanto y sumando los segundos que este presionado
               
               try {
                   
                   if (quartSecond > 0){ // cuando hayan pasado 2 segundos hare lo siguiente
                       // Aqui va el codigo del optionLetter
                       ol = new OptionLetter(me,true,me);
                       ol.setBounds(me.getX()+jButton3.getX()+10, (me.getY()+jButton3.getY())+35, 132, 41);
                       ol.setVisible(true);
                       quartSecond = 0;
                       
                       break;
                   }
                   quartSecond ++;
                   Thread.sleep(250); // Esto es para esperar cuarto segundo e ir contando
                   
               }catch (InterruptedException e){
                   
               }
           }
           
       }
    
        
    }
    
